## Laravel Task Runner

- Write shell scripts like Blade Components and run them locally or on remote servers (built on Laravel’s Process feature).
- Always activate the `laravel-task-runner-development` skill when making package-specific changes.
- For Tasks, background execution, remote connections, and testing fakes, consult:
